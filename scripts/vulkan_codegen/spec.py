"""Vulkan specification loading and parsing utilities."""

import json
import os
import xml.etree.ElementTree as ET
from pathlib import Path
from urllib.request import urlopen


def latest_github_release(repo: str, fallback: str, use_tags: bool = True) -> str:
    """Latest tag (use_tags) or stable release of a GitHub repo, or `fallback` if the query fails."""
    try:
        if use_tags:
            with urlopen(f"https://api.github.com/repos/{repo}/tags") as response:
                tags = json.loads(response.read())
            if tags:
                return tags[0]["name"]
        else:
            with urlopen(f"https://api.github.com/repos/{repo}/releases/latest") as response:
                return json.loads(response.read())["tag_name"]
    except Exception:
        pass
    return fallback


def get_latest_vulkan_spec_version(use_tags: bool = True) -> str:
    """Latest Vulkan-Docs version (e.g. 'v1.4.342'), or a known fallback."""
    return latest_github_release("KhronosGroup/Vulkan-Docs", "v1.4.338", use_tags)


def get_latest_spirv_headers_version(use_tags: bool = True) -> str:
    """Latest SPIRV-Headers version (e.g. 'vulkan-sdk-1.4.321.0'), or a known fallback."""
    return latest_github_release("KhronosGroup/SPIRV-Headers", "vulkan-sdk-1.4.321.0", use_tags)


# Versions are overridable via environment (Meson forwards the versions the build pins) and
# otherwise track the latest upstream release.
VULKAN_SPEC_VERSION = os.environ.get("VULKAN_SPEC_VERSION") or get_latest_vulkan_spec_version()
VULKAN_SPEC_URL = f"https://raw.githubusercontent.com/KhronosGroup/Vulkan-Docs/{VULKAN_SPEC_VERSION}/xml/vk.xml"

SPIRV_HEADERS_VERSION = os.environ.get("SPIRV_HEADERS_VERSION") or get_latest_spirv_headers_version()
SPIRV_GRAMMAR_URL = (
    "https://raw.githubusercontent.com/KhronosGroup/SPIRV-Headers/"
    f"{SPIRV_HEADERS_VERSION}/include/spirv/unified1/spirv.core.grammar.json"
)

